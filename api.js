/**
 * API Documentation
 * 
 * Base URL: http://localhost:3001
 * 
 * Endpoints:
 * 
 * 1. Get all medicines
 *    GET /medicines
 *    Response: Array of medicine objects
 * 
 * 2. Get a single medicine by ID
 *    GET /medicines/:id
 *    Response: Medicine object
 * 
 * 3. Add a new medicine
 *    POST /medicines
 *    Request Body: Medicine object
 *    Response: Added medicine object
 * 
 * 4. Update a medicine by ID
 *    PUT /medicines/:id
 *    Request Body: Updated medicine object
 *    Response: Updated medicine object
 * 
 * 5. Delete a medicine by ID
 *    DELETE /medicines/:id
 *    Response: Empty
 */
